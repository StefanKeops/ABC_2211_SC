package exemple_boucle_for;

/*
 * 
 * EXAMPLE BOUCLE FOR QUI COMPTE DE 1 à 50
 * 
 Variables
 
 i est un entier
 
 DEBUT DE PROGRAMME
 
 debut boucle pour
 pour i allant de 1 à 50
 		ecrire i
 fin boucle pour
 
 
 FIN DU PROGRAMME
 */

public class App {

	public static void main(String[] args) {
		
		// declarer les variables
		int i;
		
		// boucle for
		for(i=1;i<=50;i++)
		{
			System.out.println(i);
		}

	}

}
